package assignment09;

/**
 * A hacky tuple-like structure for a point in a 2D plane.
 * Primarily used if one wants to write a list of the path's points.
 *
 * @author Brayden Wright (u0895942), Abdulaziz Aljanahi (u0901606)
 */
public class Point {
    public int x;
    public int y;
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
